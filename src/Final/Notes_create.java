    private void jLabel9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9MouseClicked
        // TODO add your handling code here:
        if (noteUnderUse.isLock()) {
            noteUnderUse.setLock(false);
            if (nd.updateNote(noteUnderUse)) {
                JOptionPane.showMessageDialog(this, "Note Unlocked !");
            }
        } else {
            noteUnderUse.setLock(true);
            if (nd.updateNote(noteUnderUse)) {
                JOptionPane.showMessageDialog(this, "Note Locked !");
            }
        }
    }//GEN-LAST:event_jLabel9MouseClicked

