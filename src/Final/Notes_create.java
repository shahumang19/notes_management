    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        // TODO add your handling code here:
        if (isNew) {
            noteUnderUse.setTextData(jTextPane1.getText());
            if (nd.insertNote(noteUnderUse, userHere.getEmail())) {
                JOptionPane.showMessageDialog(this, "Note Created !");
                isNew = false;
            }
        } else {
            noteUnderUse.setTextData(jTextPane1.getText());
            if (nd.updateNote(noteUnderUse)) {
                JOptionPane.showMessageDialog(this, "Note Updated !");
            }
        }
    }//GEN-LAST:event_jLabel2MouseClicked

